# 숫자 자료형 (Int)
print(10)  # 10
print(-10)  # -10
print(3.14)  # 3.14
print(1000)  # 1000
print(5 + 2)  # 7
print(5 * 2)  # 10
print(5 * (5 + 2))  # 35

# 문자 자료형 (String)
print('Hello World!')  # Hello World!
print("Hello World!")  # Hello World!
print("Hello World! " * 3)  # Hello World! Hello World! Hello World!

# 참 / 거짓 자료형 (Boolean)
print(1 > 10)  # False
print(1 < 10)  # True
print(True)  # True
print(False)  # False
print(not True)  # False
print(not False)  # True
print(not (1 > 10))  # True
