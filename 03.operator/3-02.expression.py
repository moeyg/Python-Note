print(10 + 10 * 10)  # 110
print((10 + 10) * 10)  # 200

number = 1 + 1 * 1
print(number)  # 2

number = number + 2  # number = number(2) + 2
print("number + 2 =", number)  # 4

number += 2  # number = number(4) + 2
print(number)  # 6

number *= 2  # number = number(6) * 2
print(number)  # 12

number /= 2  # number = number(12) / 2
print(number)  # 6.0

number -= 2  # number = number(6.0) - 2
print(number)  # 4.0

number %= 3  # number = number(4.0) % 3
print(number)  # 1.0
