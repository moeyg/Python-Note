# math 라이브러리 전체를 사용
from math import *

# 절대값
print(abs(-10))  # 10 = |-10|
# 거듭제곱
print(pow(10, 2))  # 100 = 10^2

# 최대값
print(max(10, 100, 1000, 10000))  # 10000
# 최소값
print(min(10, 100, 1000, 10000))  # 10

# 반올림
print(round(4.99))  # 5
print(round(3.14))  # 3
# 소수점 이하 내림
print(floor(4.99))  # 4
print(floor(3.14))  # 3
# 소수점 이하 올림
print(ceil(4.99))  # 5
print(ceil(3.14))  # 4

# 제곱근
print(sqrt(25))  # 5.0
