# \n : 줄바꿈
print("Hello World!\nHello Python!")

# \" \' : 문장 내에서 따옴표 사용
print("Hello \"World\"!")
print("Hello \'World\'!")

# \\ : 문장 내에서 \ 사용
print("Hello \\World!\\")

# \r : \r 뒤의 문자를 문자의 개수만큼 커서를 맨 앞으로 이동
print("Hello World! \rOlleh")

# \b : 백스페이스, \b 바로 앞의 한 글자 삭제
print("Helloo\b World!")

# \t : 탭
print("Hello \tWorld!")
